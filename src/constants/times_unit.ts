// 초
export const ONE_SEC_IN_MS = 1000;

// 분
export const ONE_MIN_IN_SECS = 60;
export const ONE_MIN_IN_MS = ONE_MIN_IN_SECS * ONE_SEC_IN_MS;

// 시간
export const ONE_HOUR_IN_MINS = 60;
export const ONE_HOUR_IN_SECS = ONE_HOUR_IN_MINS * ONE_MIN_IN_SECS;
export const ONE_HOUR_IN_MS =
  ONE_HOUR_IN_MINS * ONE_MIN_IN_SECS * ONE_SEC_IN_MS;
